    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
  try {
    dbConnect db = new dbConnect();

    if (sname.getText().isEmpty() || gpa.getText().isEmpty() || annincome.getText().isEmpty() || 
        amount.getText().isEmpty() || capacity.getText().isEmpty() || des.getText().isEmpty() || 
        require.getText().isEmpty() || deadline1.getText().isEmpty() || status.getSelectedIndex() == 0) {

        JOptionPane.showMessageDialog(null, "All fields are required");
        return;
    }

    if (!gpa.getText().matches("^(Above|Below|Minimum|Between)?\\s*\\d+(\\.\\d+)?(\\s*(-|to|and)\\s*\\d+(\\.\\d+)?)?$")) {
        JOptionPane.showMessageDialog(null, "Invalid GPA format!\nExamples: 3.5, 2.0 - 3.5, Above 3.0, Between 2.0 and 3.5");
        return;
    }

    if (!annincome.getText().matches("^[a-zA-Z\\s]*\\d*[a-zA-Z\\s]*$")) {
        JOptionPane.showMessageDialog(null, "Annual income should be a number or contain descriptive text (e.g., 'Above 50000', 'No income')");
        return;
    }

    if (!amount.getText().matches("^[a-zA-Z\\s]*\\d*[a-zA-Z\\s]*$")) {
        JOptionPane.showMessageDialog(null, "Fund amount should be a number or contain descriptive text (e.g., 'Up to 50000')");
        return;
    }

    if (!capacity.getText().matches("\\d+")) {
        JOptionPane.showMessageDialog(null, "Capacity should only contain numbers");
        return;
    }

    try (Connection con = db.getConnection()) {
        String query = "UPDATE tbl_scholarship SET s_name = ?, GPA = ?, annual_income = ?, fund_amount = ?, " +
                       "capacity = ?, description = ?, requirements = ?, Deadline = ?, status = ? WHERE s_id = ?";

        try (PreparedStatement pstmt = con.prepareStatement(query)) {
            pstmt.setString(1, sname.getText());
            pstmt.setString(2, gpa.getText()); 
            pstmt.setString(3, annincome.getText()); // Store as String
            pstmt.setString(4, amount.getText()); // Store as String
            pstmt.setInt(5, Integer.parseInt(capacity.getText())); 
            pstmt.setString(6, des.getText());
            pstmt.setString(7, require.getText());
            pstmt.setString(8, deadline1.getText());
            pstmt.setString(9, (String) status.getSelectedItem());
            pstmt.setInt(10, Integer.parseInt(s_id.getText())); 

            int rowsUpdated = pstmt.executeUpdate();

            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Scholarship details updated successfully!");
                scho_table st = new scho_table();
                st.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "No records updated. Please check Scholarship ID.");
            }
        }
    }

} catch (SQLException e) {
    JOptionPane.showMessageDialog(null, "Database Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(null, "Invalid numeric input! Please enter correct values.", "Error", JOptionPane.ERROR_MESSAGE);
}
    }//GEN-LAST:event_updateMouseClicked

